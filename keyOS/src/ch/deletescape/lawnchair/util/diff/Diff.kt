package ch.deletescape.lawnchair.util.diff

abstract class Diff<K, T>(protected val first: T, protected val second: T) {
    abstract val changed: List<K>
    abstract val added: List<K>
    abstract val removed: List<K>
    val all: List<K> get() = changed + added + removed
}